+(NSDate *)changeString:(NSString *)string changeStyle:(NSString *)style
{
    NSDateFormatter *formate=[[NSDateFormatter alloc]init];
    [formate setDateFormat:style];
    //把传过来的日期字符串转化成美式日期(跟北京有时间差)
    formate.locale=[[NSLocale alloc]initWithLocaleIdentifier:@"en_US"];
    //formate.locale=[NSLocale currentLocale];//当前用户设定区域.
    NSDate *date=[formate dateFromString:string];
    return date;
}
