+(NSString *)DirectionaryType:(NSString *)DirectoryName WithDocumentsPath:(NSString *)fileName;
{
    NSString *path=NSHomeDirectory();
    NSString *stringPath= [NSString stringWithFormat:@"%@/%@/%@",path,DirectoryName,fileName];
    NSFileManager *manage=[NSFileManager defaultManager];
    if(![manage fileExistsAtPath:stringPath])
    {
        [manage createFileAtPath:stringPath contents:nil attributes:nil];
    }
    return stringPath;
}
//在Documents的文件夹下的文件夹下创建文件
+(NSString *)DirectionaryType:(NSString *)DirectoryName fileWithDirectionaryArry:(NSArray *)fileArry WithDocumentsPath:(NSString *)fileName;
{
    NSMutableString *Mstring=[[NSMutableString alloc]initWithCapacity:0];
    
    for (int i=0; i<[fileArry count]; i++) {
        [Mstring appendString:[NSString stringWithFormat:@"/%@",fileArry[i]]];
    }
    NSString *path=NSHomeDirectory();
    NSString *stringPath= [NSString stringWithFormat:@"%@/%@%@",path,DirectoryName,Mstring];
    NSFileManager *manage=[NSFileManager defaultManager];
    if(![manage fileExistsAtPath:stringPath])
    {
        [manage createDirectoryAtPath:stringPath withIntermediateDirectories:YES attributes:nil error:nil];
    }
    NSString *pathString=[NSString stringWithFormat:@"%@/%@",stringPath,fileName];
    if(![manage fileExistsAtPath:pathString])
    {
        [manage createFileAtPath:pathString contents:nil attributes:nil];
    }
    return pathString;
}
